function isWeekday(weekDay) {
    if (weekDay == 'Monday') {
        return true
    }
    else if (weekDay == 'tuesday') {
        return true
    }
    else if (weekDay == 'Wednesday') {
        return true
    }
    else if (weekDay == 'Thursday') {
        return true
    }
    else if (weekDay == 'Friday') {
        return true
    }
    else { return false }
}
export default isWeekday